/* SVG-заглушка вместо фото: градиент + эмодзи подкатегории */
function photoURL(catId, photoSeed, sub) {
  const cat = CATEGORIES.find(c => c.id === catId) || CATEGORIES[0];
  const emoji = (sub && SUB_EMOJI[sub]) || cat.emoji;
  const g = PHOTO_GRADIENTS[(photoSeed + cat.grad) % PHOTO_GRADIENTS.length];
  const rot = ((photoSeed * 7) % 24) - 12;
  const svg =
    `<svg xmlns="http://www.w3.org/2000/svg" width="640" height="480" viewBox="0 0 640 480">` +
    `<defs><linearGradient id="g" x1="0" y1="0" x2="1" y2="1">` +
    `<stop offset="0" stop-color="${g[0]}"/><stop offset="1" stop-color="${g[1]}"/>` +
    `</linearGradient></defs>` +
    `<rect width="640" height="480" fill="url(#g)"/>` +
    `<circle cx="${120 + (photoSeed * 53) % 400}" cy="${80 + (photoSeed * 31) % 320}" r="130" fill="rgba(255,255,255,0.12)"/>` +
    `<text x="320" y="265" font-size="150" text-anchor="middle" transform="rotate(${rot} 320 240)">${emoji}</text>` +
    `</svg>`;
  return 'data:image/svg+xml,' + encodeURIComponent(svg);
}

/* --- генерация объявлений --- */
function generateListings() {
  const rnd = mulberry32(20260612);
  const listings = [];
  let id = 1000;

  const pick = arr => arr[Math.floor(rnd() * arr.length)];
  const range = (a, b) => a + Math.floor(rnd() * (b - a + 1));

  for (const catId of Object.keys(TEMPLATES)) {
    const all = [...TEMPLATES[catId], ...(TEMPLATES_EXTRA[catId] || [])];
    for (const tpl of all) {
      const [sub, title, minP, maxP, opts = {}] = tpl;
      const copies = range(2, 4);
      for (let c = 0; c < copies; c++) {
        id++;
        const negotiable = opts.negotiable && rnd() < 0.75;
        let price = negotiable || maxP === 0 ? 0 : range(minP, maxP);
        if (price > 100000) price = Math.round(price / 5000) * 5000;
        else if (price > 10000) price = Math.round(price / 500) * 500;
        else if (price > 0) price = Math.round(price / 50) * 50;

        const isBishkek = rnd() < 0.6;
        const city = isBishkek ? 'Бишкек' : pick(CITIES.slice(1));
        const isBusiness = catId === 'services' || catId === 'jobs' ? rnd() < 0.45 : rnd() < 0.18;
        const photoCount = rnd() < 0.07 ? 0 : range(1, 5);
        const condition = (catId === 'services' || catId === 'jobs' || catId === 'realty' || catId === 'animals')
          ? null
          : (opts.new || rnd() < 0.22 ? 'new' : 'used');

        const descCount = range(2, 4);
        const descSet = new Set();
        while (descSet.size < descCount) descSet.add(pick(DESCRIPTION_PARTS));

        listings.push({
          id: String(id),
          title,
          price,
          priceSuffix: opts.suffix || '',
          negotiable: price === 0,
          category: catId,
          subcategory: sub,
          city,
          district: city === 'Бишкек' && rnd() < 0.7 ? pick(BISHKEK_DISTRICTS) : null,
          condition,
          description: [...descSet].join(' '),
          photoCount,
          photoSeed: id % 97,
          sellerName: isBusiness ? pick(STORE_NAMES) : pick(SELLER_NAMES),
          sellerType: isBusiness ? 'business' : 'private',
          sellerRating: Math.round((3.6 + rnd() * 1.4) * 10) / 10,
          sellerAds: range(1, isBusiness ? 120 : 14),
          sellerSinceYear: range(2019, 2026),
          postedHoursAgo: Math.floor(Math.pow(rnd(), 1.6) * 720), // свежих больше
          views: range(7, 4200),
          isVip: rnd() < 0.08,
          isUrgent: rnd() < 0.07,
          hasDelivery: (catId === 'electronics' || catId === 'fashion' || catId === 'kids' || catId === 'home' || catId === 'hobby') && rnd() < 0.4,
          phone: `+996 ${pick(['700','555','770','709','500','999','220'])} ${String(range(100,999))} ${String(range(100,999))}`,
        });
      }
    }
  }
  return listings;
}

