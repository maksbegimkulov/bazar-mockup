/* «50к», «50 тыс», «1.5 млн», «50 000» → число */
function parseAmount(numStr, unit) {
  const v = parseFloat(numStr.replace(/\s+/g, '').replace(',', '.'));
  if (isNaN(v)) return null;
  if (!unit) return Math.round(v);
  if (/^(к|k|тыс)/.test(unit)) return Math.round(v * 1000);
  if (/^(млн|миллион)/.test(unit)) return Math.round(v * 1000000);
  return Math.round(v);
}

const AMOUNT_RE = '(\\d[\\d ]*(?:[.,]\\d+)?)\\s*(к|k|тыс[а-я]*|млн[а-я]*|миллион[а-я]*)?(?= |$)';

/* --- главный парсер: текст запроса → { q, filters } --- */
/* Разрешается ли строка в категорию или каталог (бренд/модель)? Нужно, чтобы
   понять, что раскладку перевернули правильно. */
function queryResolves(str) {
  const s = normText(str);
  const words = s.split(/\s+/).filter(Boolean);
  for (const w of words) {
    // как в parseSearchQuery: сначала синоним (машина→авто), потом CAT_INTENT
    const mapped = (typeof aliasFor === 'function') ? aliasFor(w) : w;
    for (const piece of mapped.split(' ')) {
      const cands = [stemLite(piece), piece];
      if (piece.length > 5) cands.push(piece.slice(0, -1));
      if (piece.length > 6) cands.push(piece.slice(0, -2));
      if (cands.some(c => CAT_INTENT[c])) return true;
    }
    // ≥3 символов: 2-буквенные обрывки («er», «s») матчатся слишком жадно и
    // ложно «осмысливают» набор в неверной раскладке
    if (w.length >= 3 && typeof ALIAS_INDEX !== 'undefined' && (ALIAS_INDEX.has(w) || ALIAS_INDEX.has(w.replace(/\s/g, '')))) return true;
  }
  const glued = s.replace(/\s+/g, '');
  if (glued.length >= 3 && typeof ALIAS_INDEX !== 'undefined' && ALIAS_INDEX.has(glued)) return true;
  return false;
}

/* Google-стайл: человек забыл переключить раскладку. «vfibyf» (набрано в EN,
   имел в виду «машина») → переворачиваем и переразбираем. Работает в обе
   стороны, но только если ИСХОДНОЕ ничего не значит, а перевёрнутое — значит
   (чтобы не ломать латинские «camry»/«iphone»). */
function recoverLayout(raw) {
  const s = String(raw).toLowerCase().trim();
  if (!s) return raw;
  if (typeof buildAliasIndex === 'function') buildAliasIndex();
  const latin = (s.match(/[a-z]/g) || []).length;
  const cyr = (s.match(/[а-яё]/g) || []).length;
  if (latin >= 2 && cyr === 0 && typeof kbToRu === 'function') {
    const ru = kbToRu(s);
    if (ru !== s && queryResolves(ru) && !queryResolves(s)) return ru;
  }
  if (cyr >= 2 && latin === 0 && typeof kbToEn === 'function') {
    const en = kbToEn(s);
    if (en !== s && queryResolves(en) && !queryResolves(s)) return en;
  }
  return raw;
}

function parseSearchQuery(raw) {
  raw = recoverLayout(raw); // починка неверной раскладки до всего остального
  // диапазон «30000-60000», «50-80 тыс»: дефис гибнет в normText, поэтому
  // заранее переписываем в «от X .. до Y ..». НО не любой «X-Y» — цена:
  // «камри 2010-2015» (годы) и «11-64gb» (спеки) ценой НЕ являются
  const preranged = String(raw).replace(
    /(\d[\d\s]*?)\s*[-–—]\s*(\d[\d\s]*)(\s*(?:к|k|тыс[а-яa-z]*|млн[а-яa-z]*))?(?=\s|$)/i,
    (m0, a, b, unit) => {
      const A = parseInt(a.replace(/\s/g, ''), 10), B = parseInt(b.replace(/\s/g, ''), 10);
      const yearish = A >= 1950 && A <= 2035 && B >= 1950 && B <= 2035;
      if (unit) return `от ${a}${unit} до ${b}${unit}`; // юнит достаётся обеим границам
      if (yearish || A < 1000 || B < 1000) return m0;    // годы/мелкие спеки — не цена
      return `от ${a} до ${b}`;
    }
  );
  let s = ' ' + normText(preranged) + ' ';
  const filters = {};
  filters.attrs = {};
  const A = filters.attrs;
  const eat = re => { const m = s.match(re); if (m) s = s.replace(m[0], ' '); return m; };

  // КОНТЕКСТНЫЕ ЧИСЛА вытаскиваем ДО цены — иначе «до 90» (батарея) и «до 200 тыс»
  // (пробег) жадно уходят в priceMax. normText уже убрал «%», опираемся на слова.
  // Аккумулятор/АКБ: «акб 90», «батарея от 85», «с аккумулятором до 90»
  let mm = eat(/ (?:аккумулятор[а-я]*|акб|акум[а-я]*|батаре[а-я]*|battery)\s*(?:здоровь[а-я]*\s*)?(?:от|до|минимум|более|не менее)?\s*(\d{2,3})(?= )/);
  if (mm && +mm[1] >= 40 && +mm[1] <= 100) A.batteryMin = String(+mm[1]);
  // Пробег: диапазон / от / до / просто «пробег N тыс», с «км» или без
  mm = eat(/ пробег[а-я]*\s*от\s*(\d{1,3})\s*(?:тыс[а-я]*)?\s*до\s*(\d{1,3})\s*тыс[а-я]*(?:\s*км)?(?= )/);
  if (mm) { A.mileageMin = String(+mm[1] * 1000); A.mileageMax = String(+mm[2] * 1000); }
  else {
    mm = eat(/ пробег[а-я]*\s*(?:от|не менее|минимум)\s*(\d{1,3})\s*тыс[а-я]*(?:\s*км)?(?= )/);
    if (mm) A.mileageMin = String(+mm[1] * 1000);
    mm = eat(/ пробег[а-я]*\s*(?:до|не более|менее|максимум)?\s*(\d{1,3})\s*тыс[а-я]*(?:\s*км)?(?= )/);
    if (mm && !A.mileageMax) A.mileageMax = String(+mm[1] * 1000);
  }
  if (!A.mileageMax) { mm = eat(/ до\s*(\d{2,4})\s*тыс[а-я]*\s*км(?= )/); if (mm) A.mileageMax = String(+mm[1] * 1000); }
  if (!A.mileageMin) { mm = eat(/ от\s*(\d{2,4})\s*тыс[а-я]*\s*км(?= )/); if (mm) A.mileageMin = String(+mm[1] * 1000); }
  if (!A.mileageMax) { mm = eat(/ до\s*(\d{4,7})\s*км(?= )/); if (mm) A.mileageMax = String(+mm[1]); }
  // Комнаты (недвижимость): у демо-квартир НЕТ атрибута rooms (фильтр дал бы 0),
  // но в заголовках есть «N-комн.». Поэтому не фильтруем, а нормализуем любую форму
  // («2 комнатная», «от 2 комнат», «двухкомнатная») в ТЕКСТОВЫЙ токен «N комн»,
  // который совпадёт с заголовком при скоринге.
  let roomN = null;
  mm = eat(/ (одно|двух|двум|трёх|трех|четыр[её]х|пяти)[- ]?комнатн[а-я]*(?= )/);
  if (mm) roomN = { одно: '1', двух: '2', двум: '2', 'трёх': '3', трех: '3', 'четырёх': '4', четырех: '4', пяти: '5' }[mm[1]] || null;
  if (!roomN) { mm = eat(/ (?:от|до)?\s*(\d)\s*[- ]?комнатн[а-я]*(?= )/) || eat(/ (?:от|до)?\s*(\d)\s*комнат[а-я]*(?= )/) || eat(/ (?:от|до)?\s*(\d)\s*комн(?= )/); if (mm) roomN = mm[1]; }
  if (roomN) { filters.cat = filters.cat || 'realty'; s = (s + roomN + ' комн ').replace(/\s+/g, ' '); }

  // «до 50 000», «не дороже 50к», «дешевле 50 тыс», «в пределах 1 млн»
  let maxUnit = null;
  let m = s.match(new RegExp('(?:^| )(?:до|не дороже|дешевле|максимум|в пределах|за)\\s+' + AMOUNT_RE));
  if (m && !filters.priceMax) {
    const v = parseAmount(m[1], m[2]);
    if (v && v > 50) { filters.priceMax = String(v); maxUnit = m[2] || null; s = s.replace(m[0], ' '); }
  }
  // «от 100 000», «дороже 1 млн»; юнит наследуется от максимума («от 5 до 8 млн»)
  m = s.match(new RegExp('(?:^| )(?:от|дороже|не дешевле|минимум)\\s+' + AMOUNT_RE));
  if (m && !filters.priceMin) {
    const v = parseAmount(m[1], m[2] || maxUnit);
    if (v && v > 10 && (!filters.priceMax || v <= +filters.priceMax)) {
      filters.priceMin = String(v); s = s.replace(m[0], ' ');
    }
  }

  // сомы после суммы («до 50000 сом») — это валюта, а не слово запроса
  if (filters.priceMax || filters.priceMin) {
    s = s.replace(/ (?:сом[а-я]*|kgs|тенге|рубл[а-я]*|руб)(?= )/g, ' ');
  }

  // цена в долларах/баксах/у.е. → пересчёт в сомы (жаргон «до 10000 долларов»)
  if ((filters.priceMax || filters.priceMin) && / (доллар[а-я]*|бакс[а-я]*|usd|у е)(?= )/.test(s)) {
    const USD_RATE = 88; // ориентировочный курс USD→KGS
    if (filters.priceMax) filters.priceMax = String(+filters.priceMax * USD_RATE);
    if (filters.priceMin) filters.priceMin = String(+filters.priceMin * USD_RATE);
    s = s.replace(/ (доллар[а-я]*|бакс[а-я]*|usd|у е)(?= )/g, ' ');
  }

  // состояние
  if (/ (?:б у|бу|подержанн[а-я]*|с рук)(?= )/.test(s)) {
    filters.condition = 'used';
    s = s.replace(/ (?:б у|бу|подержанн[а-я]*|с рук)(?= )/g, ' ');
  }
  if (/ (?:нов(?:ый|ая|ое|ые|ую|ым|ого)|new)(?= )/.test(s)) {
    filters.condition = 'new';
    s = s.replace(/ (?:нов(?:ый|ая|ое|ые|ую|ым|ого)|new)(?= )/g, ' ');
  }

  // доставка
  if (/ доставк[а-я]*(?= )/.test(s)) {
    filters.delivery = true;
    s = s.replace(/ (?:с )?доставк[а-я]*(?= )/g, ' ');
  }

  // сортировка-намерение
  if (/ (?:недорого|дешево|подешевле|бюджетн[а-я]*|сам(?:ый|ая|ое|ые) дешев[а-я]*)(?= )/.test(s)) {
    filters.sort = 'cheap';
    s = s.replace(/ (?:недорого|дешево|подешевле|бюджетн[а-я]*|сам(?:ый|ая|ое|ые) дешев[а-я]*)(?= )/g, ' ');
  }
  if (/ (?:подороже|премиум|сам(?:ый|ая|ое|ые) дорог[а-я]*)(?= )/.test(s)) {
    filters.sort = 'expensive';
    s = s.replace(/ (?:подороже|премиум|сам(?:ый|ая|ое|ые) дорог[а-я]*)(?= )/g, ' ');
  }
  if (/ (?:свежие|за сегодня|сегодняшние)(?= )/.test(s)) {
    filters.period = '1';
    s = s.replace(/ (?:свежие|за сегодня|сегодняшние)(?= )/g, ' ');
  }

  // город (включая «в бишкеке», «в оше»)
  for (const [stem, city] of CITY_STEMS) {
    const re = new RegExp(' (?:в |г )?' + stem + '[а-я]{0,3}(?= )');
    if (re.test(s)) { filters.city = city; s = s.replace(re, ' '); break; }
  }

  // аренда/покупка (до категории, чтобы уточнить sub)
  const wantsRent = RENT_WORDS.test(s);
  const wantsBuy = BUY_WORDS.test(s);
  s = s.replace(RENT_CONSUME, ' ').replace(BUY_CONSUME, ' ');
  s = s.replace(/ (?:ищу|нужен|нужна|нужно|хочу|найди|подбери|покажи|поищи|мне|есть ли|сколько стоит|сколько стоят|стоит|стоят|почем|цена|цену)(?= )/g, ' ');

  // «подарок/сюрприз» — это СОВЕТУЮЩИЙ интент, а не товар. Если искать его как
  // текст («подарок до 9000»), находится пусто. Помечаем запрос подарочным и
  // убираем из текста и слово-интент, и адресата — остаётся только бюджет,
  // по которому вернём идеи (советующий блок Дианы включится по isAdvisoryQuery).
  if (/ (?:подар[а-я]*|сюрприз[а-я]*|презент[а-я]*)(?= )/.test(s)) {
    filters._gift = true;
    s = s.replace(/ (?:в |на )?(?:подар[а-я]*|сюрприз[а-я]*|презент[а-я]*)(?= )/g, ' ');
    // адресат подарка — тоже не товар («подарок девушке» не должен искать «девушку»)
    s = s.replace(/ (?:девушк|парн[юя]|жен[ена]|муж[ау]|мам[еуы]|пап[еуы]|друг[ауе]?|подруг|коллег|ребен|ребён|дет[яеи]|брат|сестр|бабушк|дедушк|сын[уа]?|доч|начальник|шеф|любим)[а-я]*(?= )/g, ' ');
  }

  // категория по ключевым словам; токен ВСЕГДА остаётся в q —
  // специфичные слова («электросамокат») сужают выдачу внутри категории,
  // а по генерикам («телефоны») сработает category-fallback в applyFilters
  const intentOf = piece => {
    const cands = [stemLite(piece), piece];
    // обрезки только для длинных слов — иначе «котел»→'кот' (Кошки),
    // «кедр»→'кед' (Обувь), «мотор»→'мото', «столб»→'стол'
    if (piece.length > 5) cands.push(piece.slice(0, -1));
    if (piece.length > 6) cands.push(piece.slice(0, -2));
    for (const c of cands) if (CAT_INTENT[c]) return CAT_INTENT[c];
    return null;
  };

  const words = s.split(/\s+/).filter(Boolean);
  const rest = [];
  for (const w of words) {
    if (STOP_TOKENS.has(w)) continue;
    // интент ищем по алиас-форме, но в q оставляем СЛОВО ЮЗЕРА —
    // транслит применит prepQueryTokens при скоринге, а юзер видит свой текст
    const mapped = aliasFor(w);
    for (const piece of mapped.split(' ')) {
      const intent = intentOf(piece);
      if (intent && !filters.cat) {
        filters.cat = intent[0];
        if (intent[1]) filters.sub = intent[1];
      }
    }
    rest.push(w);
  }

  // недвижимость: уточняем продажу/аренду
  if (filters.cat === 'realty' && !filters.sub) {
    if (wantsRent) filters.sub = 'Аренда квартир';
    else if (wantsBuy) filters.sub = 'Продажа квартир';
  }
  // «снять/аренда» без слова «квартира»
  if (!filters.cat && wantsRent) { filters.cat = 'realty'; filters.sub = 'Аренда квартир'; }

  let qRest = rest.join(' ').trim();

  /* --- NLU поверх базового парсера: бренд/модель/поколение из справочника
     + числовые характеристики (память, диагональ, батарея, видеокарта,
     пробег, год, запас хода) + исключения «не самсунг». Работает по остатку,
     чтобы не спорить с уже снятыми ценой/городом/состоянием. --- */
  if (typeof nluParse === 'function' && qRest) {
    try {
      // NLU получает ИСХОДНУЮ строку: базовый парсер вычищает служебные слова
      // (включая «не»), а без них ломались отрицания «не самсунг»
      const n = nluParse(raw);
      if (n.cat && !filters.cat) { filters.cat = n.cat; if (n.sub) filters.sub = n.sub; }
      if (n.sub && !filters.sub && filters.cat === n.cat) filters.sub = n.sub;

      const attrs = Object.assign({}, filters.attrs || {}, n.attrs || {});
      if (n.brand) attrs.brand = n.brand;
      if (n.model) attrs.model = n.model;
      if (n.gen) attrs.gen = n.gen;
      if (Object.keys(attrs).length) filters.attrs = attrs;

      if (n.exclude && n.exclude.length) filters.exclude = n.exclude;
      if (n.layoutFixed) filters._layoutFixed = n.layoutFixed;

      // Фаззи по опечатке в ОДНУ букву («саммунг»→Samsung, «айфн»→Apple, «тойта»→Toyota).
      // Запускаем, пока не найден бренд/модель. Ложняки на обычных словах гасим двумя
      // независимыми предохранителями:
      //   • категория уже известна → принимаем ТОЛЬКО если она совпадает с брендовой
      //     («стол»=Мебель ≠ транспорт(Kia Soul) → отказ; «саммунг»=Телефоны=Samsung → ок);
      //   • категория неизвестна → требуем общий префикс ≥3 с ближайшим алиасом
      //     («айфн»↔«айфон» делят «айф» → ок; «дрель»↔«дель» лишь «д» → отказ).
      const a0 = filters.attrs || {};
      if (!a0.brand && !a0.model && typeof fuzzyBrandMatch === 'function') {
        const cpLen = (a, b) => { let i = 0; while (i < a.length && i < b.length && a[i] === b[i]) i++; return i; };
        const ws = qRest.split(/\s+/).filter(w => w.length >= 4).sort((x, y) => y.length - x.length);
        for (const w of ws) {
          // не «чиним» слово, которое само по себе легитимно (категория/синоним):
          // «авто»→Aito, «сони»→бренд, «мото»… — реальные слова, не опечатки бренда
          if (CAT_INTENT[w] || CAT_INTENT[stemLite(w)] || QUERY_ALIASES[w]) continue;
          const fz = fuzzyBrandMatch(w);
          if (!fz || !fz.brand) continue;
          // категория известна → бренд принимаем при совпадении категории;
          // категория НЕизвестна → нужен общий префикс ≥3 И (популярный бренд ИЛИ
          // длинное слово ≥5): иначе редкая модель ловит обычное короткое слово
          // («кедр»↔«кеди»=VW Caddy делят «кед», но Caddy не популярна → отказ).
          const ok = filters.cat
            ? filters.cat === fz.cat
            : (cpLen(w, fz._cand || '') >= 3 && (fz.pop || w.length >= 5));
          if (!ok) continue;
          if (!filters.cat && fz.cat) { filters.cat = fz.cat; if (fz.sub) filters.sub = fz.sub; }
          const at = Object.assign({}, filters.attrs || {});
          at.brand = fz.brand; if (fz.kind === 'model' && fz.model) at.model = fz.model;
          filters.attrs = at;
          qRest = qRest.replace(new RegExp('(?:^| )' + w.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') + '(?= |$)'), ' ').trim();
          break;
        }
      }
      // текстом для скоринга оставляем только то, что НЕ распознал ни базовый
      // парсер (цена/город/состояние), ни NLU (бренд/модель/характеристики)
      const keep = new Set(String(n.rest || '').split(/\s+/).filter(Boolean));
      qRest = qRest.split(/\s+/).filter(w => keep.has(w)).join(' ');
    } catch (e) { /* NLU не должен ломать обычный поиск */ }
  }

  return { q: qRest, filters };
}

/* --- скоринг объявления по токенам запроса --- */

/* предлоги и мусорные частицы — не несут поискового смысла */
const STOP_TOKENS = new Set(['для', 'в', 'на', 'с', 'по', 'из', 'под', 'у', 'о', 'от', 'до', 'и', 'к', 'же', 'ли', 'бы']);

function lev1(a, b) {
  // расстояние Левенштейна ≤1? (быстрая проверка)
  if (a === b) return true;
  const la = a.length, lb = b.length;
  if (Math.abs(la - lb) > 1) return false;
  let i = 0, j = 0, edits = 0;
  while (i < la && j < lb) {
    if (a[i] === b[j]) { i++; j++; continue; }
    if (++edits > 1) return false;
    if (la > lb) i++;
    else if (lb > la) j++;
    else { i++; j++; }
  }
  if (i < la || j < lb) edits++;
  return edits <= 1;
}

/* перестановка соседних букв («айфно» → «айфон») */
function swapEq(a, b) {
  if (a.length !== b.length) return false;
  let i = 0;
  while (i < a.length && a[i] === b[i]) i++;
  if (i >= a.length - 1) return false;
  if (a[i] !== b[i + 1] || a[i + 1] !== b[i]) return false;
  return a.slice(i + 2) === b.slice(i + 2);
}

const fuzzyEq = (a, b) => lev1(a, b) || swapEq(a, b);

function tokenizeWords(s) {
  return normText(s).split(/\s+/).filter(t => t.length > 0);
}

/* индекс объявления: токены заголовка и «прочего» (раздел/описание/город).
   КРИТИЧНО: кэш живёт в отдельной Map, НЕ на объекте объявления —
   иначе он сериализуется в localStorage (bazar_my), Sets превращаются
   в {} и после перезагрузки весь скоринг падает с TypeError. */
const LISTING_IDX = new Map();

function listingIndex(l) {
  let idx = LISTING_IDX.get(l.id);
  if (!idx) {
    const title = new Set();
    for (const w of tokenizeWords(l.title)) { title.add(w); title.add(stemLite(w)); }
    const other = new Set();
    const cat = catById(l.category);
    const src = `${l.subcategory} ${cat ? cat.name : ''} ${l.city} ${l.description}`;
    for (const w of tokenizeWords(src)) { other.add(w); other.add(stemLite(w)); }
    idx = { title, other, titleArr: [...title] };
    LISTING_IDX.set(l.id, idx);
  }
  return idx;
}

/* алиас: точный → по префиксу («айф» → iphone, пока юзер печатает) → fuzzy («айфно») */
function aliasFor(w) {
  if (QUERY_ALIASES[w]) return QUERY_ALIASES[w];
  if (w.length >= 3 && /^[а-я]+$/.test(w)) {
    for (const k in QUERY_ALIASES) {
      if (k.length > w.length && k.startsWith(w)) return QUERY_ALIASES[k];
    }
  }
  // fuzzy-опечатки только для слов ≥5 с совпадающей первой буквой — иначе
  // короткие обычные слова прилипают к брендам («пони»→sony, «маки»→max)
  if (w.length >= 5 && /^[а-я]+$/.test(w)) {
    for (const k in QUERY_ALIASES) {
      if (k.length >= 5 && k[0] === w[0] && fuzzyEq(w, k)) return QUERY_ALIASES[k];
    }
  }
  return w;
}

/* запрос → токены для скоринга (с алиасами и стемами) */
function prepQueryTokens(q) {
  const out = [];
  for (const w of tokenizeWords(q)) {
    const mapped = aliasFor(w);
    for (const piece of mapped.split(' ')) {
      if (STOP_TOKENS.has(piece)) continue;
      out.push({ raw: piece, stem: stemLite(piece) });
    }
  }
  return out;
}

/* счёт за один токен в одном объявлении; 0 = не найден */
function tokenScore(tok, idx) {
  if (idx.title.has(tok.raw) || idx.title.has(tok.stem)) return 3;
  if (tok.raw.length >= 3) {
    for (const t of idx.titleArr) {
      if (t.length >= 3 && (t.startsWith(tok.raw) || tok.raw.startsWith(t) && t.length >= 4)) return 2;
    }
  }
  if (tok.raw.length >= 5) {
    for (const t of idx.titleArr) {
      if (t.length >= 5 && fuzzyEq(tok.raw, t)) return 1.6;
    }
  }
  if (idx.other.has(tok.raw) || idx.other.has(tok.stem)) return 1.1;
  if (tok.raw.length >= 4) {
    for (const t of idx.other) {
      if (t.length >= 4 && t.startsWith(tok.raw)) return 0.7;
    }
  }
  return 0;
}

/* суммарный счёт; minMatched — сколько токенов обязаны совпасть */
function scoreListing(l, qTokens, minMatched) {
  const idx = listingIndex(l);
  let score = 0, matched = 0;
  for (const tok of qTokens) {
    const s = tokenScore(tok, idx);
    if (s > 0) matched++;
    score += s;
  }
  if (matched < minMatched) return 0;
  return score + matched * 0.5;
}

/* сколько совпадений требуем: все, но при ≥3 токенах прощаем один промах */
function requiredMatches(qTokens) {
  return qTokens.length >= 3 ? qTokens.length - 1 : qTokens.length;
}
